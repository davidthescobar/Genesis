# Matrix
# Matrix are nested arrarys (lists)

matrix = [
  'test',
  [1,2,3],
  [4,5,6],
  [7,8,9]
] # This is 2-dimensional. 

print(matrix[1][1]) # [] = outer list, [] = nested list

basket = ['bananas', 'blueberries', ['apples', ['oranges',['grapes']]]
] # Is this 4-dimensional?

print(basket[0])
print(basket[1])
print(basket[2])
print(basket[2][0])
print(basket[2][1][0])
